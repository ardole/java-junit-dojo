package fr.dojo.calculator.basic;

class Calculator {

    long add(int x, int y) {
        return x + y;
    }

    long substract (int x, int y) {
        return x - y;
    }

    long multiply (int x, int y) {
        return x * y;
    }

    long divide (int x, int y) {
        return x / y;
    }

}
